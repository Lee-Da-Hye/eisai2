            <li><h3>Eisai's Corporate Philosophy - hhc(human health care)</h3></li>
            <li>Eisai는 hhc(human health care)라는 기업철학을 바탕으로, 인류의 건강에 공헌하고자 최선을 다하고 있습니다.</li>
            <li>우리는 환자와 가족을 health care의 핵심 요소라는 명확한 이해를 바탕으로 그들의 상황을 공감하기 위해 직원이 먼저 환자에게 가까이
            <br>다가가서 그들의 입장에서 생각하고 느끼며 배우는 것이 중요하다고 생각합니다. 이것이 Eisai 내 모든 활동의 출발점이 되는 개념입니
            다. <br>따라서 Eisai의 모든 직원은 근무 시간의 1%를 환자와 함께 보내고 있습니다.
            </li>
            <li>회사의 철학은 Eisai 내 모든 직원들이 이해하고 내재화가 되어 있으며, 이러한 이해는 일상적인 비즈니스 활동에서 공유되고 구현되며,
                더 나아가 환자와 가족의 관점에서 그들의 요구에 대응하기 위해 개발하고, 이러한 대응에 대한 사회적 혜택을 검증하여 다른 사람보다
                먼저 세상에 제공함으로써<br>사회에 대한 우리의 의무를 이행하고자 합니다. 이것이 Eisai가 실현하고자 하는 진정한 가치, hhc 입
                니다.</li>
            <li>Eisai 철학의 로고는 현대 간호 역사에서 공중보건의 발전에 큰 기여를 한 플로렌스 나이팅게일(Florence Nightingale, 1820-1910)의
                <br>서명을 모델로 하여, 그녀의 삶과 헌신에서 얻은 영감을 hhc 서체에 반영하였습니다.
            <br>hhc 로고를 볼 때마다 Eisai의 직원들은 일상 업무 속에서 그녀라면 어떻게 했을까 라고 되묻곤 합니다.</li>
        </ul>
